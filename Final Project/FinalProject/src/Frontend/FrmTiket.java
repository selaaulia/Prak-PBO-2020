    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Tiket tik = new Tiket();

        boolean res = checkInput(txtKonser.getText(), txtHarga.getText(), txtTanggal.getText());
        boolean tgl = this.isValidDate(txtTanggal.getText());

        if (res) {
            if (tgl == false) {
                JOptionPane.showMessageDialog(this, "Format tanggal salah");
            } else {
                tik.setIdtiket(Integer.parseInt(txtIdTiket.getText()));
                tik.setKonser(txtKonser.getText());
                tik.setKategori(cmbKategori.getSelectedItem().toString());
                tik.setHarga(Integer.parseInt(txtHarga.getText()));
                tik.setTgl(txtTanggal.getText());
                tik.save();
                txtIdTiket.setText(Integer.toString(tik.getIdtiket()));
                tampilkanData();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Semua data tiket harus diisi!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

