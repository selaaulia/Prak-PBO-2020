    public final void tampilkanData() {
        String[] kolom = {"ID", "Konser", "Kategori", "Harga", "Tanggal"};
        ArrayList<Tiket> list = new Tiket().getAll();
        Object rowData[] = new Object[5];

        tblTiket.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Tiket tik : list) {
            rowData[0] = tik.getIdtiket();
            rowData[1] = tik.getKonser();
            rowData[2] = tik.getKategori();
            rowData[3] = tik.getHarga();
            rowData[4] = tik.getTgl();

            ((DefaultTableModel) tblTiket.getModel()).addRow(rowData);
        }
    }

    public final void cari(String keyword) {
        String[] kolom = {"ID", "Konser", "Kategori", "Harga", "Tanggal"};
        ArrayList<Tiket> list = new Tiket().search(keyword);
        Object rowData[] = new Object[5];

        tblTiket.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Tiket tik : list) {
            rowData[0] = tik.getIdtiket();
            rowData[1] = tik.getKonser();
            rowData[2] = tik.getKategori();
            rowData[3] = tik.getHarga();
            rowData[4] = tik.getTgl();

            ((DefaultTableModel) tblTiket.getModel()).addRow(rowData);
        }
    }

    public boolean checkInput(String konser, String harga, String tanggal) {
        boolean res = true;
        if (konser.equals("") || harga.equals("") || tanggal.equals("")) {
            res = false;
        }
        return res;
    }

    public boolean isValidDate(String inDate) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        dateFormat.setLenient(false);
        try {
            dateFormat.parse(inDate.trim());
        } catch (ParseException pe) {
            return false;
        }
        return true;
    }

